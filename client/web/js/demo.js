function isVideoURL(url) {
  // List of common video file extensions
  const videoExtensions = [
    ".mp4",
    ".avi",
    ".mkv",
    ".mov",
    ".wmv",
    ".flv",
    ".m4v",
    ".webm",
  ];

  // Check if the URL ends with a video extension
  for (const ext of videoExtensions) {
    if (url.endsWith(ext)) {
      return true;
    }
  }

  // If the URL doesn't end with a video extension, it's not a video URL
  return false;
}

function isHTMLURL(url) {
  // Check if the URL contains the ".html" extension
  return url.includes(".html");
}
function ispdfURL(url) {
  // Check if the URL contains the ".html" extension
  return url.includes(".pdf");
}

const subjectOrder = [
  "science",
  "physics",
  "chemistry",
  "biology",
  "maths",
  "mathematics",
];
function sort(a, b) {
  const indexA = subjectOrder.indexOf(a.subject.toLowerCase());
  const indexB = subjectOrder.indexOf(b.subject.toLowerCase());

  // If the subject is found in the predefined order, use the index for sorting.
  // If not found, treat it as larger and push it to the end.
  if (indexA === -1) return 1; // If `a.subject` is not in subjectOrder, push it to the end.
  if (indexB === -1) return -1; // If `b.subject` is not in subjectOrder, push it to the end.

  return indexA - indexB; // Sort based on the predefined index.
}

function sortSubjectWise(subjects) {
  const orderedSubs = [];
  const unorderedSubs = [];
  subjects.forEach((subject) => {
    if (subjectOrder.includes(subject.subject.toLowerCase())) {
      orderedSubs.push(subject);
    } else {
      unorderedSubs.push(subject);
    }
  });
  console.log([...orderedSubs.sort(sort), ...unorderedSubs]);
  return [...orderedSubs.sort(sort), ...unorderedSubs]
}

